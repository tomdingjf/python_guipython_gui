# 快捷键	                        说明
# Ctrl + Q	                    快速查看文档
# Ctrl + 鼠标左键	            跳转到函数或类等的定义位置
# Ctrl + /	                    行注释/取消注释（可选中多行）
# Ctrl + Alt + L	            代码格式化
# Tab / Shift + Tab	            缩进/取消缩进（可选中多行）
# Ctrl + D	                    复制选定的区域（一行或多行）
# Ctrl + Y	                    删除当前行
# Shift + Enter	                任意位置换行 （很实用）
# Ctrl + Shift + F7	            将当前单词在整个文件中高亮，F3 移动到下一个，ESC 取消高亮
# 双击 Shift	                    快速查找
# Alt + Enter	                智能语法提示
# Alt + Shift + up/down	        当前行上移或下移动
# Shift + F6	                方法或变量重命名 (很实用)
# Ctrl + Alt + 空格	            代码补全
# Shift + F10	                运行程序
# Shift + F9	                调试程序（debug模式）
# F8	                        单步执行
